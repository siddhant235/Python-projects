from PIL import Image,ImageFilter
img=Image.open('original1.jpg')
filtered_img=img.filter(ImageFilter.BLUR)
filtered_img.save("blur_org1.png","png")
filtered_img.show()
converted_img=img.convert('L')
converted_img.save("gray_org1.png","png")
converted_img.show()
rotated_img=img.rotate(90)
rotated_img.save("rotated-org1.png","png")
rotated_img.show()
box=(100,100,400,400)
cropped=img.crop(box)
cropped.save("croporg1.png","png")
cropped.show()
print(img)
print(img.size)
print(img.format)
print(img.mode)